const truthyValues = new Set(['1', 'true', 'yes', 'y', 'on']);
const falsyValues = new Set(['0', 'false', 'no', 'n', 'off']);

function isRecord(value) {
  return Boolean(value) && typeof value === 'object' && !Array.isArray(value);
}

function envFlag(name, fallback = false) {
  const raw = process.env[name];
  if (raw === undefined) {
    return fallback;
  }
  const normalized = raw.trim().toLowerCase();
  if (truthyValues.has(normalized)) {
    return true;
  }
  if (falsyValues.has(normalized)) {
    return false;
  }
  return fallback;
}

function positiveNumberEnv(name, fallback, minimum) {
  const parsed = Number(process.env[name]);
  if (!Number.isFinite(parsed)) {
    return fallback;
  }
  return Math.max(minimum, parsed);
}

function writeJson(filePath, payload) {
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  fs.writeFileSync(filePath, `${JSON.stringify(payload, null, 2)}\n`, 'utf8');
}

function shellQuote(value) {
  const normalized = typeof value === 'string' ? value : String(value ?? '');
  return `'${normalized.replace(/'/gu, "'\\''")}'`;
}

function writeSelectedModelEnv({ selectedProvider, selectedModel }) {
  fs.mkdirSync(path.dirname(SELECTED_ENV_PATH), { recursive: true });
  fs.writeFileSync(
    SELECTED_ENV_PATH,
    [
      '# Generated by pnpm test:provider-readiness after a passed provider-backed process check.',
      '# Safe to source locally; it contains model names only, not credentials or provider output.',
      `OPS_LIVE_TELEGRAM_PROCESS_MODEL=${shellQuote(selectedModel)}`,
      `OPS_PROVIDER_READINESS_MODEL=${shellQuote(selectedModel)}`,
      `OPS_PROVIDER_READINESS_SELECTED_PROVIDER=${shellQuote(selectedProvider ?? 'unknown')}`,
      ''
    ].join('\n'),
    'utf8'
  );
}

function readDotenvValue(name) {
  const dotenvPath = path.join(REPO_ROOT, '.env');
  if (!fs.existsSync(dotenvPath)) {
    return null;
  }
  const lines = fs.readFileSync(dotenvPath, 'utf8').split(/\r?\n/u);
  for (const line of lines) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) {
      continue;
    }
    const separator = trimmed.indexOf('=');
    if (separator === -1) {
      continue;
    }
    const key = trimmed.slice(0, separator).trim();
    if (key !== name) {
      continue;
    }
    let value = trimmed.slice(separator + 1).trim();
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }
    return value.length > 0 ? value : null;
  }
  return null;
}

function readControlPlaneConfig(configPath = DEFAULT_CONFIG_PATH) {
  try {
    const parsed = JSON.parse(fs.readFileSync(configPath, 'utf8'));
    return isRecord(parsed) ? parsed : {};
  } catch {
    return {};
  }
}

function resolveApiToken(config) {
  const candidates = [
    process.env.OPS_PROVIDER_READINESS_API_TOKEN,
    process.env.OPS_API_TOKEN,
    readDotenvValue('OPS_API_TOKEN'),
    isRecord(config.server) ? config.server.apiToken : null
  ];
  for (const candidate of candidates) {
    const token = typeof candidate === 'string' ? candidate.trim() : '';
    if (token.length > 0 && !token.includes('change-me')) {
      return token;
    }
  }
  return null;
}

function resolveGatewayBaseUrl(config) {
  const explicit =
    process.env.OPS_PROVIDER_READINESS_BASE_URL?.trim() ||
    process.env.OPS_GATEWAY_BASE_URL?.trim();
  if (explicit) {
    return explicit.replace(/\/+$/u, '');
  }
  const server = isRecord(config.server) ? config.server : {};
  const hostValue = typeof server.host === 'string' && server.host.trim() ? server.host.trim() : '127.0.0.1';
  const host = hostValue === '0.0.0.0' ? '127.0.0.1' : hostValue;
  const port = Number.isFinite(Number(server.port)) ? Number(server.port) : 8788;
  return `http://${host}:${port.toString()}`;
}

function authHeaders(token) {
  return {
    authorization: `Bearer ${token}`,
    'x-api-token': token,
    'x-ops-role': 'admin'
  };
}

function splitModelList(value) {
  if (typeof value !== 'string') {
    return [];
  }
  return value
    .split(/[,\n]/u)
    .map((entry) => entry.trim())
    .filter((entry) => entry.length > 0);
}

function uniqueStrings(values) {
  const seen = new Set();
  const result = [];
  for (const value of values) {
    const key = value.toLowerCase();
    if (seen.has(key)) {
      continue;
    }
    seen.add(key);
    result.push(value);
  }
  return result;
}

function resolveConfiguredProcessModelCandidates() {
  const explicit = process.env.OPS_PROVIDER_READINESS_MODEL?.trim();
  const configuredCandidates = splitModelList(process.env.OPS_PROVIDER_READINESS_MODEL_CANDIDATES);
  const liveTelegramCandidates = splitModelList(process.env.OPS_LIVE_TELEGRAM_PROCESS_MODEL_CANDIDATES);
  if (explicit) {
    return {
      explicit,
      configured: uniqueStrings([explicit, ...configuredCandidates, ...liveTelegramCandidates])
    };
  }
  const liveTelegramExplicit = process.env.OPS_LIVE_TELEGRAM_PROCESS_MODEL?.trim();
  return {
    explicit: liveTelegramExplicit || null,
    configured: uniqueStrings([
      ...(liveTelegramExplicit ? [liveTelegramExplicit] : []),
      ...configuredCandidates,
      ...liveTelegramCandidates
    ])
  };
}

function hashValue(value) {
  const normalized = typeof value === 'string' ? value.trim() : '';
  if (!normalized) {
    return null;
  }
  return crypto.createHash('sha256').update(normalized).digest('hex').slice(0, 16);
}

async function requestJson(url, { method = 'GET', headers = {}, body, timeoutMs = 20000 } = {}) {
  const controller = new AbortController();
  const timer = setTimeout(() => controller.abort(), timeoutMs);
  try {
    const response = await fetch(url, {
      method,
      headers: {
        ...(body !== undefined ? { 'content-type': 'application/json' } : {}),
        ...headers
      },
      body: body !== undefined ? JSON.stringify(body) : undefined,
      signal: controller.signal
    });
    const raw = await response.text();
    let payload = {};
    if (raw.trim().length > 0) {
      try {
        payload = JSON.parse(raw);
      } catch {
        payload = { raw: redactText(raw, 500) };
      }
    }
    if (!response.ok) {
      const message = isRecord(payload) && typeof payload.error === 'string' ? payload.error : `HTTP ${response.status}`;
      const error = new Error(message);
      error.statusCode = response.status;
      error.payload = payload;
      throw error;
    }
    return payload;
  } catch (error) {
    if (error instanceof Error && error.name === 'AbortError') {
      throw new Error(`Request timed out after ${timeoutMs.toString()}ms`);
    }
    throw error;
  } finally {
    clearTimeout(timer);
  }
}

async function recordStep(report, id, run) {
  const startedAt = new Date().toISOString();
  const startedMs = Date.now();
  try {
    const result = await run();
    report.steps.push({
      id,
      status: 'passed',
      startedAt,
      finishedAt: new Date().toISOString(),
      durationMs: Date.now() - startedMs
    });
    return result;
  } catch (error) {
    report.steps.push({
      id,
      status: 'failed',
      startedAt,
      finishedAt: new Date().toISOString(),
      durationMs: Date.now() - startedMs,
      error: redactError(error)
    });
    throw error;
  }
}

function summarizeEffectiveRouting(payload) {
  const routes = Array.isArray(payload.routes) ? payload.routes.filter(isRecord) : [];
  const firstRoute = routes[0] ?? {};
  const selected = isRecord(firstRoute.selected) ? firstRoute.selected : null;
  const checks = Array.isArray(firstRoute.checks) ? firstRoute.checks.filter(isRecord) : [];
  return {
    available: routes.length > 0,
    reason: typeof firstRoute.reason === 'string' ? firstRoute.reason : null,
    requestedModel: typeof firstRoute.requestedModel === 'string' ? firstRoute.requestedModel : null,
    selected: selected !== null,
    selectedProvider: selected && typeof selected.provider === 'string' ? selected.provider : null,
    selectedModel: selected && typeof selected.model === 'string' ? selected.model : null,
    selectedProfileId: selected && typeof selected.authProfileId === 'string' ? selected.authProfileId : null,
    checks: checks.slice(0, 8).map((check) => ({
      model: typeof check.model === 'string' ? check.model : null,
      provider: typeof check.provider === 'string' ? check.provider : null,
      authProfileId: typeof check.authProfileId === 'string' ? check.authProfileId : null,
      eligible: typeof check.eligible === 'boolean' ? check.eligible : null,
      reason: typeof check.reason === 'string' ? check.reason : null
    }))
  };
}

function providerModelCandidateFromRegistryEntry(entry) {
  if (!isRecord(entry)) {
    return null;
  }
  const model = typeof entry.model === 'string' ? entry.model.trim() : '';
  if (!model) {
    return null;
  }
  const provider = typeof entry.provider === 'string' ? entry.provider.trim().toLowerCase() : '';
  if (provider !== 'openrouter' && provider !== 'google') {
    return null;
  }
  const runtimes = Array.isArray(entry.allowedRuntimes)
    ? entry.allowedRuntimes.map((runtime) => (typeof runtime === 'string' ? runtime.trim().toLowerCase() : ''))
    : [];
  if (!runtimes.includes('process')) {
    return null;
  }
  return model;
}

function providerModelsFromRegistry(registry) {
  if (!isRecord(registry)) {
    return [];
  }
  const entries = Array.isArray(registry.entries) ? registry.entries : [];
  const entryCandidates = entries
    .map(providerModelCandidateFromRegistryEntry)
    .filter((entry) => typeof entry === 'string' && entry.length > 0);

  const providerLists = [];
  const providers = isRecord(registry.providers) ? registry.providers : {};
  for (const provider of ['openrouter', 'google']) {
    const values = Array.isArray(providers[provider]) ? providers[provider] : [];
    providerLists.push(...values.filter((entry) => typeof entry === 'string' && entry.trim().length > 0));
  }

  return uniqueStrings([...entryCandidates, ...providerLists]);
}

async function discoverProcessModelCandidates({ report, gatewayBaseUrl, headers, timeoutMs }) {
  const query = new URLSearchParams({
    runtime: 'process',
    policy: 'orchestrator'
  });
  const payload = await recordStep(report, 'candidate-discovery', () =>
    requestJson(`${gatewayBaseUrl}/api/llm/routing/effective?${query.toString()}`, {
      headers,
      timeoutMs: Math.min(timeoutMs, 15000)
    })
  );
  const registryCandidates = providerModelsFromRegistry(payload.registry);
  const routes = Array.isArray(payload.routes) ? payload.routes.filter(isRecord) : [];
  const routeCandidates = [];
  for (const route of routes) {
    const primaryModel = typeof route.primaryModel === 'string' ? route.primaryModel.trim() : '';
    if (primaryModel) {
      routeCandidates.push(primaryModel);
    }
    const selected = isRecord(route.selected) ? route.selected : {};
    const selectedModel = typeof selected.model === 'string' ? selected.model.trim() : '';
    if (selectedModel) {
      routeCandidates.push(selectedModel);
    }
  }
  return uniqueStrings([...routeCandidates, ...registryCandidates]);
}

function providerCandidatePriority(model) {
  const normalized = typeof model === 'string' ? model.trim().toLowerCase() : '';
  if (!normalized) {
    return 100;
  }
  if (normalized.includes('gpt-5-mini')) {
    return 10;
  }
  if (normalized.includes('gpt-4.1-mini')) {
    return 20;
  }
  if (normalized.includes('gemini-2.5-flash')) {
    return 30;
  }
  if (normalized.includes('gemini') && normalized.includes('flash')) {
    return 40;
  }
  if (normalized.startsWith('openrouter/')) {
    return 50;
  }
  return 60;
}

function orderDiscoveredCandidates(candidates) {
  return [...candidates].sort((left, right) => {
    const priorityDelta = providerCandidatePriority(left) - providerCandidatePriority(right);
    return priorityDelta !== 0 ? priorityDelta : left.localeCompare(right);
  });
}

function mergeProcessModelCandidates({ configured, discovered, defaults }) {
  return uniqueStrings([
    ...configured,
    ...orderDiscoveredCandidates(discovered),
    ...defaults
  ]);
}

